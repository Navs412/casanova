export const PAUSE_MODE_ADDENDUM = `This is a PAUSE MODE request. The user is in the middle of a live interaction. Respond in 150 words maximum. Lead with the action. Format: [What to say/do] + [Why] + [What to watch for]. No preamble.`;

export function buildSystemPrompt(opts: {
  archetype?: string | null;
  artsPracticed?: string[];
  sessionCount?: number;
  mode?: string;
}): string {
  let prompt = SYSTEM_PROMPT + '\n' + PRIVACY_ADDENDUM;

  const contextParts: string[] = [];
  if (opts.archetype) contextParts.push(`The user's archetype is ${opts.archetype}.`);
  if (opts.artsPracticed?.length) contextParts.push(`Arts they've practiced: ${opts.artsPracticed.join(', ')}.`);
  if (opts.sessionCount !== undefined) contextParts.push(`Session count: ${opts.sessionCount}.`);
  if (opts.mode) contextParts.push(`This is a ${opts.mode} session.`);

  if (contextParts.length > 0) {
    prompt += '\n\n## USER CONTEXT\n' + contextParts.join(' ');
  }

  if (opts.mode === 'pause') {
    prompt += '\n\n' + PAUSE_MODE_ADDENDUM;
  }

  return prompt;
}
